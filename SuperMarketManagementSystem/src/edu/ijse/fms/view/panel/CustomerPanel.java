    private void btnCustomerSaveMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnCustomerSaveMouseClicked
        String customerId = txtCustomerId.getText();
        String customerName = txtCustomerName.getText();
        String customerGender = "male";

        if (btnCustomerMale.isSelected()) {
            customerGender = "male";
        }
        if (btnCustomerFemale.isSelected()) {
            customerGender = "Female";
        }
        String customerTelNumber = txtCustomerTelNumber.getText();
        String customerAddress = txtCustomerAddress.getText();
        CustomerDTO customerDto = new CustomerDTO(customerId, customerName, customerGender, customerTelNumber, customerAddress);
        if (customerId.isEmpty()) {
            txtCustomerId.setBorder(new LineBorder(Color.red, 1));
        }
        if (customerName.isEmpty()) {
            txtCustomerName.setBorder(new LineBorder(Color.red, 1));
        }
        if (!customerTelNumber.matches("\\d{3}-\\d{7}")) {
            txtCustomerTelNumber.setBorder(new LineBorder(Color.red, 1));
        }
        if (customerAddress.isEmpty()) {
            txtCustomerAddress.setBorder(new LineBorder(Color.red, 1));
        }
        if (customerId.isEmpty() == false && customerName.isEmpty() == false && customerTelNumber.matches("\\d{3}-\\d{7}") == false && customerAddress.isEmpty() == false) {
            try {
                boolean isAdded = customerController.add(customerDto);
                System.out.println(isAdded);
                if (isAdded) {
                    JOptionPane.showMessageDialog(this, "Added Sucessfully");
                    textFieldNull();
                    getID();
                } else {
                    JOptionPane.showMessageDialog(this, "Oops Added Fail", "Error", JOptionPane.ERROR_MESSAGE);
                }
            } catch (SQLException ex) {
                String errorMsg;
                switch (ex.getErrorCode()){
                    case 1062:
                        errorMsg = "Duplicate entry has been found. Please enter valid Customer Id to proceed.";
                        break;
                    default:
                        errorMsg = "Customer has been failed to add";
                        break;
                }
                JOptionPane.showMessageDialog(this, errorMsg, "Error", JOptionPane.ERROR_MESSAGE);

            } catch (Exception ex) {
                Logger.getLogger(CustomerPanel.class.getName()).log(Level.SEVERE, null, ex);
            }

        }


    }//GEN-LAST:event_btnCustomerSaveMouseClicked

