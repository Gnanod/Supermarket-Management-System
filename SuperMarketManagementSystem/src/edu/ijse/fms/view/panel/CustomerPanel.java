    private void btnCustomerUpdateMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnCustomerUpdateMouseClicked
        String customerId = txtCustomerId.getText();
        String customerName = txtCustomerName.getText();
        String supplierGender = "Male";
        if (btnCustomerMale.isSelected()) {
            supplierGender = "Male";
        }
        if (btnCustomerMale.isSelected()) {
            supplierGender = "Female";
        }
        String address = txtCustomerAddress.getText();
        String telNumber = txtCustomerTelNumber.getText();
        CustomerDTO customerDto = new CustomerDTO(customerId, customerName, supplierGender, telNumber,address);
        try {
            boolean isUpdated = customerController.update(customerDto);
            if (isUpdated) {
                JOptionPane.showMessageDialog(this, "Updated sucessfully");

            } else {
                JOptionPane.showMessageDialog(this, "Oops Added Fail", "Error", JOptionPane.ERROR_MESSAGE);
            }
        } catch (SQLException ex) {
            String errorMsg;
            switch (ex.getErrorCode()) {
                case 1062:
                    errorMsg = "Duplicate entry has been found. Please enter valid Customer Id to proceed.";
                    break;
                default:
                    errorMsg = "Customer has been failed to add";
                    break;
            }
            JOptionPane.showMessageDialog(this, errorMsg, "Error", JOptionPane.ERROR_MESSAGE);

        } catch (Exception ex) {
            Logger.getLogger(CustomerPanel.class.getName()).log(Level.SEVERE, null, ex);
        }

    }//GEN-LAST:event_btnCustomerUpdateMouseClicked

