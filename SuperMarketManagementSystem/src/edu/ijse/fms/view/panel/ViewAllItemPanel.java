private void viewAllItems() {
        try {

            ArrayList<ViewItemNameBatchDTO> itemNameList = viewItemNameBatchController.getAll();
            //  System.out.println(itemNameList + "mmmmmmmmmmmmmmmmmmmmmm");
            if (itemNameList == null) {
                return;
            }
            for (ViewItemNameBatchDTO itemNameList1 : itemNameList) {
                Object[] ob = {itemNameList1.getBatchId(), itemNameList1.getItemName(), itemNameList1.getAmount(), itemNameList1.getUnitPrice()};
                
                dtm.addRow(ob);
            }
        } catch (Exception ex) {
            Logger.getLogger(pnlViewAllItem.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
