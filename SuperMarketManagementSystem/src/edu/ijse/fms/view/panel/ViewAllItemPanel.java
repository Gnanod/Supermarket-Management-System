    private void txtSearchItemKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtSearchItemKeyReleased
            String type = txtSearchItem.getText();
            try{
                ArrayList<ViewItemNameBatchDTO> list = viewItemNameBatchController.searchAll(type);
                if(list == null){
                    JOptionPane.showMessageDialog(this,"Item Not Found");
                }else{
                    dtm.setRowCount(0);
                    for (ViewItemNameBatchDTO list1 : list) {
//                        tblItem.remo
                        Object [] ob = {list1.getBatchId(),list1.getItemName(),list1.getAmount(),list1.getUnitPrice()};
                        dtm.addRow(ob);
                    }
                }
                
            } catch (Exception ex) {
            Logger.getLogger(ViewAllItemPanel.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_txtSearchItemKeyReleased


