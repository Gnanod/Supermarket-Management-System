    private void txtSearchCustomerKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtSearchCustomerKeyReleased
        String type = txtSearchCustomer.getText();

        try{
            ArrayList<CustomerDTO> customerList = customerController.searchCustomers(type);

            if(customerList== null){
                JOptionPane.showMessageDialog(this,"Supplier Not Found");
            }else{
                dtmCustomer.setRowCount(0);
                for(CustomerDTO customerDto :customerList){
                    Object[]rowData ={customerDto.getCustomerId(),customerDto.getCustomerName(),customerDto.getCustomerGender(),customerDto.getCustomerTelNumber(),customerDto.getCustomerAddress()};
                    dtmCustomer.addRow(rowData);
                }
            }

        } catch (Exception ex) {
            Logger.getLogger(SearchCustomerDialog.class.getName()).log(Level.SEVERE, null, ex);
        } 
    }//GEN-LAST:event_txtSearchCustomerKeyReleased

