public void getAllCustomer(){
           try{
               ArrayList<CustomerDTO> customerList = customerController.getAll();
               if(customerList== null){
                   return;
               }
               for(CustomerDTO customer :customerList){
                    Object[]rowData ={customer.getCustomerId(),customer.getCustomerName(),customer.getCustomerGender(),customer.getCustomerTelNumber(),customer.getCustomerAddress()};
                    dtmCustomer.addRow(rowData);
                           
               }
           } catch (Exception ex) {
            Logger.getLogger(SearchDialog.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
