    private void tblUserMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblUserMouseClicked
        String userId = tblUser.getValueAt(tblUser.getSelectedRow(), 0).toString();
        String userName = tblUser.getValueAt(tblUser.getSelectedRow(), 1).toString();
        String userType = tblUser.getValueAt(tblUser.getSelectedRow(), 2).toString();
        String gender = tblUser.getValueAt(tblUser.getSelectedRow(), 3).toString();
        String password = tblUser.getValueAt(tblUser.getSelectedRow(),4).toString();
        String telNumber = tblUser.getValueAt(tblUser.getSelectedRow(),5).toString();
        String address = tblUser.getValueAt(tblUser.getSelectedRow(), 6).toString();
        
        UserDTO user = new UserDTO(userId, userName, userType,gender, password,telNumber, address);
        up.setUsetDetails(user);
        dispose();

    }//GEN-LAST:event_tblUserMouseClicked

