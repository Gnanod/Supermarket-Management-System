    private void txtSearchUserKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtSearchUserKeyReleased
        String type = txtSearchUser.getText();

        try {
            ArrayList<UserDTO> userList = userController.searchSuppliers(type);

            if (userList == null) {
                JOptionPane.showMessageDialog(this, "Supplier Not Found");
            } else {
                dtmUser.setRowCount(0);
                for (UserDTO userDto :userList) {
                    Object[] rowData = {userDto.getUserId(), userDto.getUserName(),userDto.getUserType(), userDto.getUserGender(), userDto.getUserPassword(), userDto.getUserTelNumber(), userDto.getUserAddress()};
                    dtmUser.addRow(rowData);
                }
            }

        } catch (Exception ex) {
            Logger.getLogger(SearchDialog.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_txtSearchUserKeyReleased

