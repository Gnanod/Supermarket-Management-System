private void loadUserData(){
    try{
        ArrayList<UserDTO> userList = userController.getAll();
        if(userList==null){
            return;
        }
        for (UserDTO userList1 : userList) {
            Object [] ob = {userList1.getUserId(),userList1.getUserName(),userList1.getUserType(),userList1.getUserGender(),userList1.getUserPassword(),userList1.getUserTelNumber(),userList1.getUserTelNumber()};
            dtmUser.addRow(ob);
        }
    }   catch (Exception ex) {
            Logger.getLogger(SearchUserDialog.class.getName()).log(Level.SEVERE, null, ex);
        }
}
