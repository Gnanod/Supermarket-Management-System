    private void txtSearchSupplierKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtSearchSupplierKeyReleased
        String type = txtSearchSupplier.getText();

        try {
            ArrayList<SupplierDTO> supplierList = supplierController.searchSuppliers(type);

            if (supplierList == null) {
                JOptionPane.showMessageDialog(this, "Supplier Not Found");
            } else {
                dtmSupplier.setRowCount(0);
                for (SupplierDTO supplierDto : supplierList) {
                    Object[] rowData = {supplierDto.getSupplierId(), supplierDto.getSupplierName(), supplierDto.getSupplierGender(), supplierDto.getSupplierTelNumber(), supplierDto.getSupplierCompanyName(), supplierDto.getSupplierCompanyName(), supplierDto.getSupplierCompanyAddress(), supplierDto.getSupplierAddress()};
                    dtmSupplier.addRow(rowData);
                }
            }

        } catch (Exception ex) {
            Logger.getLogger(SearchDialog.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_txtSearchSupplierKeyReleased

    private void tblSupplierMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblSupplierMouseClicked
        String supplierId = tblSupplier.getValueAt(tblSupplier.getSelectedRow(), 0).toString();
        String supplierName = tblSupplier.getValueAt(tblSupplier.getSelectedRow(), 1).toString();
        String gender = tblSupplier.getValueAt(tblSupplier.getSelectedRow(), 2).toString();
        String telNumber = tblSupplier.getValueAt(tblSupplier.getSelectedRow(), 3).toString();
        String companyName = tblSupplier.getValueAt(tblSupplier.getSelectedRow(), 4).toString();
        String companyAddress = tblSupplier.getValueAt(tblSupplier.getSelectedRow(), 5).toString();
        String address = tblSupplier.getValueAt(tblSupplier.getSelectedRow(), 6).toString();

        SupplierDTO supplier = new SupplierDTO(supplierName, supplierId, gender, telNumber, companyName, companyAddress, address);
        sp.setSupplierDetails(supplier);
        dispose();
    }//GEN-LAST:event_tblSupplierMouseClicked

