    public void getAllSupplier() {
        try {
            ArrayList<SupplierDTO> supplierList = supplierController.getAll();
            if (supplierList == null) {
                return;
            }
            for (SupplierDTO supplier : supplierList) {
                Object[] rowData = {supplier.getSupplierId(), supplier.getSupplierName(), supplier.getSupplierGender(), supplier.getSupplierTelNumber(), supplier.getSupplierAddress(), supplier.getSupplierCompanyName(), supplier.getSupplierCompanyAddress()};
                dtmSupplier.addRow(rowData);

            }
        } catch (Exception ex) {
            Logger.getLogger(SearchDialog.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
